        </div>

        <div class="panel-body">
								<p>Inspektorat Jenderal selaku aparat pengawasan internal pemerintah mendukung penuh upaya Kemendikbud sekaligus mengawasi implementasi program Kemendikbud agar berjalan sesuai rencana dan &nbsp;mencapai tujuan serta <em>outcome</em> yang diharapkan dengan dukungan alokasi anggaran yang telah ditetapkan.</p>
<p>Inspektorat mengawal pelaksanaan anggaran pendidikan dan melakukan upaya preventif, detektif, represif, simultan dan berkelanjutan melalui beberapa program seperti audit reguler, audit investigasi, pemantauan dan pendampingan,&nbsp;<em>review,&nbsp;</em>evaluasi, dan pengawasan lainnya.</p>
<p>Dalam pelaksanaan tugasnya Inspektorat Jenderal juga mengawal dan mengawasi dana BOS,DAK, Sertifikasi Guru, BSM, dan lainnya. Ada 3 rencana capaian keberhasilan yang dicanangkan, yaitu :</p>
<ul>
<li>tercapainya opini Wajar Tanpa Pengecualian (WTP) oleh BPK atas laporan keuangan Kemendikbud;</li>
<li>terwujudnya Kemendikbud sebagai Wilayah Bebas Korupsi (WBK);</li>
<li>terjalinnya sinergitas antara sesama lembaga pemerintah, baik lembaga pengawasan, maupun lembaga penegak hukum dalam mengawasi implementasi program kerja Kemendikbud.</li>
</ul>
<p>Inspektorat Jenderal Kemendikbud memiliki struktur organisasi yaitu lima&nbsp;Eselon II yang terdiri dari satu Sekretariat, dan empat Inspektorat.</p>
<ul>
<li>Unit Sekretariat bertugas melakukan manajemen operasional kantor Inspektorat Jenderal meliputi bagian perencanaan dan penganggaran, bagian pengolahan laporan dan pengawasan, bagian hukum dan kepegawaian, dan bagian umum.</li>
<li>Ke empat Inspektorat terdiri dari 3 Inspektorat Bidang, dan 1 Inspektorat Investigasi.
<ul>
<li><strong>Inspektorat I</strong>, melakukan pengawasan terhadap program Kemendikbud di bidang&nbsp;<strong>Pendidikan Anak Usia Dini, Non Formal dan Informal, Bahasa, dan Kebudayaan</strong></li>
<li><strong>Inspektorat II</strong>, melakukan pengawasan terhadap program Kemendikbud di bidang <strong>Pendidikan Dasar dan Menengah, dan Badan Penelitian dan Pengembangan</strong></li>
<li><strong>Inspektorat III</strong>, melakukan pengawasan terhadap program Kemendikbud di bidang&nbsp;<strong>Guru dan Tenaga Kependidikan dan Kebudayaan</strong></li>
<li><strong>Inspektorat Investigasi</strong>, &nbsp;menindaklanjuti pengaduan masyarakat dengan metode Investigatif, atau melakukan pengawasan dengan tujuan tertentu atau berdasarkan perintah Menteri Pendidikan dan Kebudayaan.</li>
</ul>
</li>
</ul>
<p>Inspektorat Jenderal Kemendikbud adalah satuan kerja di Lingkungan Kementerian Pendidikan dan Kebudayaan. Inspektorat Jenderal berada di bawah dan bertanggung jawab kepada Menteri.</p>
<p>Dalam melaksanakan tugas, Inspektorat Jenderal menyelenggarakan fungsi :</p>
<ul>
<li>penyusunan kebijakan teknis pengawasan intern di lingkungan Kementerian Pendidikan dan Kebudayaan</li>
<li>pelaksanaan pengawasan internal di lingkungan Kementerian Pendidikan dan Kebudayaan terhadap&nbsp;<strong>kinerja dan keuangan melalui audit, reviu, evaluasi, pemantauan, dan kegiatan evaluasi lainnya</strong>;</li>
<li>pelaksanaan pengawasan untuk tujuan tertentu atas penugasan Menteri Pendidikan dan Kebudayaan</li>
<li>pelaksanaan administrasi Inspektorat Jenderal; dan</li>
<li>pelaksanaan fungsi lain yang diberikan oleh Menteri.</li>
</ul>
