        <div class="panel-body">
			<p>Sejarah awal berdirinya Inspektorat Jenderal dapat dikatakan bersamaan dengan lahirnya Republik Indonesia. Pada masa kemerdekaan, para Bapak Bangsa
			  <em>(Founding Fathers)</em> membentuk berbagai kementerian termasuk Kementerian Pendidikan dan Pengajaran sebagai bagian utama dari perangkat pemerintahan nasional. Para pendiri republik yang berpandangan luas dan jauh ke depan menyadari akan pentingnya pengawasan.
			  </p>
			  <p>Dalam Kementerian Pendidikan dan Pengajaran terdapat unsur pengawasan sebagai salah satu fungsi manajemen yang tak terpisahkan dari fungsi perencanaan dan pelaksanaan pendidikan. Pengawasan pendidikan merupakan bagian integral dari upaya mencerdaskan kehidupan bangsa sebagaimana diamanatkan UUD 1945. Pengawasan pendidikan mulai dirintis dalam bentuk inspeksi pada tahun 1948. Pada saat itu pengawasan pendidikan dilakukan dalam bentuk inspeksi-inspeksi yangdimaksudkan untuk membina dan mengawasi teknis pendidikan dan kebudayaan di tingkat pusat.
			  </p>
			  <p>Tahun 1949 mulai dibentuk lembaga Inspeksi Daerah. Pada tahun 1949 mulai dibentuk lembaga Inspeksi Taman Kanak-kanak/Sekolah Rakyat di kabupaten, selanjutnya lembaga Inspeksi Taman Kanak-kanak/Sekolah Dasar di kecamatan. Di pusat dan provinsi dibentuk lembaga Inspeksi Sekolah Menengah Pertama, Inspeksi Sekolah Menengah Atas, Inspeksi Pendidikan Kejuruan, Inspeksi Pendidikan Jasmani, dan Inspeksi Kebudayaan. Tahun 1966 Inspeksi Pusat berubah menjadi Direktorat dan di provinsi dibentuk Kantor Daerah.
			  </p>
