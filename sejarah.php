            <div class="well">
              <p>Memasuki tahun 1968 penggunaan istilah pengawasan dan pemeriksaan mulai dikenal secara meluas dengan akronim WASRIK. Tradisi pengawasan dan pemeriksaan di Indonesia banyak dipengaruhi oleh gaya kolonial Belanda yang dilakukan oleh para Inspecteur yang kemudian diindonesiakan terminologinya menjadi Inspektur. Semula para Inspektur ini di zaman Belanda lebih diterapkan di kalangan kepolisian dengan melakukan pemeriksaan (inspeksi), namun kemudian diperluas menjadi Inspektur dalam segala bidang pemerintahan yang bertugas untuk melakukan pengendalian.
			  </p>
			  <p>Satuan kerja yang menangani pengawasan pada saat itu berupa bagian bernama Bagian Pengawasan dan Pemeriksaan yang selanjutnya ditingkatkan menjadi suatu Biro Pengawasan dan Pemeriksaan Administratif (BPPA) yang berada di dalam struktur organisasi Sekretariat Jenderal. Sebagai sebuah Biro di bawah Sekretariat Jenderal tentunya tidak memberikan mandat legitimasi yang memadai dalam hal independensi untuk mengawasi kinerja seluruh Departemen Pendidikan dan Kebudayaan. Mandat seperti itu terasa hanya terbatas pada wilayah Sekretariat Jenderal saja, tetapi tanggung jawab pengawasan dipaksakan untuk seluruh Departemen. Hal ini tentunya bukan merupakan kondisi ideal yang kondusif dalam melaksanakan mandat pengawasan Departemen.
			  </p>
			  <p>Selanjutnya saat yang sangat bersejarah adalah ditingkatkannya status organisasi yang semula Biro di bawah Sekretariat Jenderal menjadi Inspektorat Jenderal pada tahun 1969 yang dilegalisasi dengan Keputusan Menteri Pendidikan dan Kebudayaan Nomor 37 tahun 1969 tanggal 27 Mei 1969. Dengan peningkatan status tersebut maka posisi Inspektorat Jenderal disejajarkan dengan unit-unit utama (eselon I) Depdikbud lainnya. Hal ini membuat independensi Inspektorat Jenderal meningkat dan lebih memberdayakan dalam melakukan pengawasan dan pemeriksaan di lingkungan Depdikbud secara utuh.
